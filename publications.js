function showAllPublications() {
  const target = document.getElementById("publications-list");

  if (target) {
    target.innerHTML = publications.join("\n");

    if (window.MathJax && window.MathJax.typesetPromise) {
      MathJax.typesetPromise([target]);
    }
  }
}

function showRandomPublication() {
  const target = document.getElementById("publication-highlight");

  if (target && publications.length > 0) {
    const randomNumber = Math.floor(Math.random() * publications.length);
    target.innerHTML = publications[randomNumber];

    if (window.MathJax && window.MathJax.typesetPromise) {
      MathJax.typesetPromise([target]);
    }
  }
}

showAllPublications();
